
public class Main {

    public static void main(String[] args) {
        
        // Declarando duas variáveis inteiras n1 e n2 e atribuindo valores a elas
        int n1 = 89;
        int n2 = 60;
        
        // Imprimindo o resultado da operação bitwise "AND" entre n1 e n2
        System.out.println(n1 & n2);
        
        // Imprimindo o resultado da operação bitwise "OR" entre n1 e n2
        System.out.println(n1 | n2);
        
        // Imprimindo o resultado da operação bitwise "XOR" (OU exclusivo) entre n1 e n2
        System.out.println(n1 ^ n2);
    }

}
